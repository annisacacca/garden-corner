import { createClient } from "@supabase/supabase-js";

// Add these to your .env file:
// VITE_SUPABASE_URL=https://your-project.supabase.co
// VITE_SUPABASE_ANON_KEY=your-anon-key
const supabaseUrl = import.meta.env.VITE_SUPABASE_URL;
const supabaseAnonKey = import.meta.env.VITE_SUPABASE_ANON_KEY;

if (!supabaseUrl || !supabaseAnonKey) {
  console.warn(
    "[scrapbook] Missing Supabase env vars. Add VITE_SUPABASE_URL and VITE_SUPABASE_ANON_KEY to your .env file."
  );
}

export const supabase = createClient(supabaseUrl, supabaseAnonKey);

export const SCRAPBOOK_BUCKET = "scrapbook-images";
export const SCRAPBOOK_TABLE = "scrapbook_posts";

/**
 * Uploads an image file to the scrapbook-images bucket and returns its public URL.
 */
export async function uploadScrapbookImage(file) {
  const fileExt = file.name.split(".").pop();
  const fileName = `${crypto.randomUUID()}.${fileExt}`;
  const filePath = `${fileName}`;

  const { error: uploadError } = await supabase.storage
    .from(SCRAPBOOK_BUCKET)
    .upload(filePath, file, {
      cacheControl: "3600",
      upsert: false,
    });

  if (uploadError) throw uploadError;

  const {
    data: { publicUrl },
  } = supabase.storage.from(SCRAPBOOK_BUCKET).getPublicUrl(filePath);

  return publicUrl;

  
}
